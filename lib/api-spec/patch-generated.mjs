/**
 * Post-codegen patch: orval v8.5.3 generates an empty authUser.ts for the
 * AuthUser schema (the TypeScript interface is dropped while the zod schema is
 * correctly inlined in api.ts).  This script writes the correct interface so
 * the rest of the type graph can resolve it.
 */
import { writeFileSync, mkdirSync, readdirSync, statSync, readFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..", "..");

const typesDir = resolve(
  root,
  "lib",
  "api-zod",
  "src",
  "generated",
  "types",
);

const authUserTs = resolve(typesDir, "authUser.ts");

const content = `/**
 * Generated by orval v8.5.3 🍺
 * Do not edit manually.
 * Api
 * API specification for Overhype.me
 * OpenAPI spec version: 0.1.0
 */
import type { AuthUserMembershipTier } from './authUserMembershipTier';
import type { AuthUserUserRole } from './authUserUserRole';

export interface AuthUser {
  id: string;
  email?: string | null;
  firstName?: string | null;
  lastName?: string | null;
  profileImageUrl?: string | null;
  displayName?: string | null;
  membershipTier?: AuthUserMembershipTier;
  isAdmin?: boolean | null;
  isRealAdmin?: boolean | null;
  pronouns?: string | null;
  userRole?: AuthUserUserRole;
  realUserRole?: AuthUserUserRole;
  captchaVerified?: boolean | null;
}
`;

mkdirSync(typesDir, { recursive: true });
writeFileSync(authUserTs, content, "utf8");
console.log("patch-generated: wrote authUser.ts");

// Fix empty generated type files — orval v8.5.3 emits zero-byte .ts files for
// many schemas (the zod schema is correctly inlined in api.ts but the TS
// interface is dropped). For files we have a known-good template for, restore
// the proper interface content. For unknown empty files, fall back to
// `export {};` so TypeScript treats them as valid ES modules instead of
// ambient scripts (which would cause TS2306 errors when barrel-exported).
const templatesDir = resolve(__dirname, "type-templates");
const templateFiles = new Set(
  readdirSync(templatesDir).filter((f) => f.endsWith(".ts")),
);
const allTypeFiles = readdirSync(typesDir).filter((f) => f.endsWith(".ts") && f !== "index.ts");
let templatesRestored = 0;
let emptyFixed = 0;
for (const file of allTypeFiles) {
  const fullPath = resolve(typesDir, file);
  if (statSync(fullPath).size === 0) {
    if (templateFiles.has(file)) {
      const templateContent = readFileSync(resolve(templatesDir, file), "utf8");
      writeFileSync(fullPath, templateContent, "utf8");
      templatesRestored++;
    } else {
      writeFileSync(fullPath, "export {};\n", "utf8");
      emptyFixed++;
    }
  }
}
if (templatesRestored > 0) console.log(`patch-generated: restored ${templatesRestored} type file(s) from templates`);
if (emptyFixed > 0) console.log(`patch-generated: added export{} to ${emptyFixed} empty type file(s)`);

// Fix empty files in api-client-react/src/generated/ — same problem: orval emits
// zero-byte api.schemas.ts when no inline schemas exist.
const apiClientGeneratedDir = resolve(root, "lib", "api-client-react", "src", "generated");
const allClientFiles = readdirSync(apiClientGeneratedDir).filter((f) => f.endsWith(".ts"));
let clientEmptyFixed = 0;
for (const file of allClientFiles) {
  const fullPath = resolve(apiClientGeneratedDir, file);
  if (statSync(fullPath).size === 0) {
    writeFileSync(fullPath, "export {};\n", "utf8");
    clientEmptyFixed++;
    console.log(`patch-generated: fixed empty api-client-react file: ${file}`);
  }
}

// Fix api-zod/src/index.ts — orval generates `export * from "./generated/types"` which
// clashes with the zod const names in `./generated/api` (TS2308 ambiguity errors).
// We instead do an explicit `export type { AuthUser }` for only the types that are
// directly imported by consuming packages (api-server uses AuthUser in authMiddleware/auth).
// All other types can be derived via `z.infer<typeof Schema>` from the zod exports.
const apiZodIndexTs = resolve(root, "lib", "api-zod", "src", "index.ts");
const apiZodIndexContent = [
  'export * from "./generated/api";',
  'export type { AuthUser } from "./generated/types/authUser";',
  'export type { AuthUserMembershipTier } from "./generated/types/authUserMembershipTier";',
  'export type { AuthUserUserRole } from "./generated/types/authUserUserRole";',
  'export * from "./memeAspectRatios";',
  'export * from "./templateGrammar";',
  "",
].join("\n");
writeFileSync(apiZodIndexTs, apiZodIndexContent, "utf8");
console.log("patch-generated: fixed api-zod/src/index.ts");
